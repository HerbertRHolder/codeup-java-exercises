package movies;

public class Movie {
    private String name;
    private String category;
    public Movie(String n, String c) {
        this.name = n;
        this.category = c;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getCategory() {
        return category;
    }
    public void setCategory(String category) {
        this.category = category;
    }







}